
package org.freeknight.music.task.parser;

class SingerOutModel
{

	private long		id;

	private String	mid;

	private String	name;

	public long getId ( )
	{
		return id;
	}

	public void setId (
			long id )
	{
		this.id = id;
	}

	public String getMid ( )
	{
		return mid;
	}

	public void setMid (
			String mid )
	{
		this.mid = mid;
	}

	public String getName ( )
	{
		return name;
	}

	public void setName (
			String name )
	{
		this.name = name;
	}

}
